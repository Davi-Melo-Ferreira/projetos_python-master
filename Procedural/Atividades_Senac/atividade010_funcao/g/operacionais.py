def somar(a, b):
    soma = a + b
    return soma

def subtrair(a, b):
    subtrai = a - b
    return subtrai

def multiplicar(a, b):
    produto = a * b
    return produto

def dividir(a, b):
    divisao = a / b
    return divisao

def dividir_inteiro(a, b):
    divisao_inteira = a // b
    return divisao_inteira

def resto_divisao(a, b):
    resto = a % b
    return resto